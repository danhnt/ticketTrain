package train.domain.model;

public class Ghe {
	private int maGhe;
	private int soGhe;
	private int maToa;
	private String moTa;
	public int getMaGhe() {
		return maGhe;
	}
	public void setMaGhe(int maGhe) {
		this.maGhe = maGhe;
	}
	public int getSoGhe() {
		return soGhe;
	}
	public void setSoGhe(int soGhe) {
		this.soGhe = soGhe;
	}
	public int getMaToa() {
		return maToa;
	}
	public void setMaToa(int maToa) {
		this.maToa = maToa;
	}
	public String getMoTa() {
		return moTa;
	}
	public void setMoTa(String moTa) {
		this.moTa = moTa;
	}
}
