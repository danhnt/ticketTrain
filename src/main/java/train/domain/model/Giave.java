package train.domain.model;

public class Giave {
private String maTau;
private int maToa;
private String moTa;
private float giaVe;
public String getMaTau() {
	return maTau;
}
public void setMaTau(String maTau) {
	this.maTau = maTau;
}
public int getMaToa() {
	return maToa;
}
public void setMaToa(int maToa) {
	this.maToa = maToa;
}
public String getMoTa() {
	return moTa;
}
public void setMoTa(String moTa) {
	this.moTa = moTa;
}
public float getGiaVe() {
	return giaVe;
}
public void setGiaVe(float giaVe) {
	this.giaVe = giaVe;
}

}
